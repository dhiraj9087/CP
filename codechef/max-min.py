y=int(input())
for y in range(y):
    b,c,d=map(int,input().split())
    if(b>c and b>c):
        if(c<d):
            print(b-c)
        else:
            print(b-d)    
    elif(c>b and c>d):
        if(b<d):
            print(c-b)
        else:
            print(c-d)
    else:
        if(b<c):
            print(d-b)
        else:
            print(d-c)                   